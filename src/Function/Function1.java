package Function;

public class Function1 {

    // Method untuk menambahkan dua bilangan
    public int add(int a, int b) {
        int hasil = 0;
        hasil = a + b;
        return hasil;
    }

    // Method untuk mengalikan dua bilangan
    public int multiply(int a, int b) {
        int hasil = 0;
        hasil = a * b;
        return hasil;
    }
}
